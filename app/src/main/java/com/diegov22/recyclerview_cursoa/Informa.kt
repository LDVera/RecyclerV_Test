class Informa (nombre:String, precio:Double, raiting:Float, foto:Int) {

    var nombre =  ""
    var precio = 0.0
    var raiting = 0.0F
    var foto = 0

    init{
        this.nombre = nombre
        this.precio = precio
        this.raiting = raiting
        this.foto = foto
    }

}